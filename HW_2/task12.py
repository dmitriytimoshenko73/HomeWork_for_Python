'''
Задача 12: Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница. Петя помогает Кате по математике.
Он задумывает два натуральных числа X и Y (X,Y≤1000), а Катя должна их отгадать. Для этого Петя делает две 
подсказки. Он называет сумму этих чисел S и их произведение P. Помогите Кате отгадать задуманные Петей числа.
'''
s = int(input())
p = int(input())

# Ищем два числа x и y, такие что x + y = s и x * y = p
# Перебираем все возможные значения x от 1 до s-1
for x in range(1, s):
    # Если x является делителем p, то y = p // x
    if p % x == 0:
        y = p // x
        # Если x + y = s, то мы нашли искомые числа
        if x + y == s:
            print(x, y)
            break

        

